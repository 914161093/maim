在市内免税店购买的商品，在出境2天前都可以办理退货或者换货。离开韩国的当天在机场免税店购买的商品，或者是在免税品取货点取货后，有的品牌是可以受理退货换货，而有的则不可以，具体情况需向购买物品的免税店或免税品取货点确认。

</strong>
