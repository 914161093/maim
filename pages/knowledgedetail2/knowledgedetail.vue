	<strong class="title">A：1）在哪里取货？
如果是在市内免税店购买的商品，在机场或港口接受完出国（这里的国是指韩国）审查后，就可前往各免税店的取货点领取自己的商品。取货时不要忘记携带物品交换券、护照和登机卡（只能自己去取货，别人是不能代取的）。取货时请仔细确认购买商品是否无误，并在核对数量、尺寸及颜色后签字（取货时要注意商品的质量）。有时候可能领货的地点人群比较拥挤，所以建议提前1小时去提货。在免税店领取的商品是可以直接带上飞机的，即使是液体也是可以带上去的。但是你的商品必须是密封的才行（也就是说你不能把商品给拆开。）
Ps：免税店中的韩国本土品牌购买后可直接提货使用，国际品牌需要到机场提货。

</strong>
