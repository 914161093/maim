中档化妆品品牌有：菲诗小铺(THE FACE SHOP) 、伊蒂之屋(ETUDE HOUSE)、谜尚(MISSHA) 、悦诗风吟(innisfree) 、托尼魅力(TONYMOLY) 、思亲肤(SKIN FOOD) 、自然乐园(NATURE REPUBLIC) 等 。
</strong>
